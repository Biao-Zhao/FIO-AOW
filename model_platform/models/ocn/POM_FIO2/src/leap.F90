 SUBROUTINE LEAP(YEAR,MON)
 IMPLICIT NONE
 INTEGER YEAR,MON(12)
 INTEGER MYOUNG1,MYOUNG2,MYOUNG3
! DATA MON/31,28,31,30,31,30,31,31,30,31,30,31/
 MON(1)=31;MON(2)=28;MON(3)=31;MON(4)=30
 MON(5)=31;MON(6)=30;MON(7)=31;MON(8)=31
 MON(9)=30;MON(10)=31;MON(11)=30;MON(12)=31
 MYOUNG1=MOD(YEAR,4)
 MYOUNG2=mod(YEAR,100)
 MYOUNG3=mod(YEAR,400)
 IF(MYOUNG1==0.AND.MYOUNG2/=0.OR.MYOUNG3==0)THEN
 MON(2)=29
 ELSE
 MON(2)=28
 END IF
 END
